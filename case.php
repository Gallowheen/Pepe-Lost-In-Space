    <section class="technologies">
      <div class="container">
        <h2 class="title title--case">Technologies</h2>
        <p class="p p--first p--case">
          With the help of several tutorial, I was able to learn and make my own game. I’ve learned principe such as velocity, hitbox, using sprites, moving image in realtime and much more, object-oriented programming, ...
        </p>
        <p class="p p--case">
          The game itself uses the HTML5’s element canvas. I’ve learned to use it through the <a class="link" href="https://developer.mozilla.org/fr/docs/Tutoriel_canvas"> MDN tutorial</a> : it’s pretty basic but it’s really helpful ! It also works with javascript / JQuery while the user interface is a mix of HTML / CSS. 
        </p>
        <p class="p p--case">
          There is a leaderboard, which means it was my duty to ensure the score sent by the player was legit. So I’ve started to search online for the best possible way to introduce to the game a working anti-cheat solution. Then I found a script by <a class="link" href="https://github.com/zswang/jdetects" target="_blank">zswang</a> on github which allow the browser to detect if the console is opened. Sadly after some test it appears that this solution was working for Chrome only. After some research I’ve made my own solution for the other browsers. It’s clearly not the best but it’s working fine without any more slow down in game !
        </p>
        <p class="p p--case">
          Speaking of leaderboard, to update and send the score I had to use a database (SQL) and some script (PHP) to update and retrieve the informations as well. It was clear for me, since we used PHP and SQL in our Data Play project, that it was something I wanted to learn and work with in my future projects.
        </p>
